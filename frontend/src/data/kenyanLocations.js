// Get all towns as a flat array for search
export const getAllTowns = () => {
  const allTowns = [];
  Object.values(kenyanTowns).forEach(towns => {
    allTowns.push(...towns);
  });
  return [...new Set(allTowns)].sort(); // Remove duplicates and sort
};

// Get towns for a specific county
export const getTownsForCounty = (county) => {
  return kenyanTowns[county] || [];
};

